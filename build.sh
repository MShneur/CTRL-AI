#!/bin/bash
# CTRL-AI build script — generates llms-full.txt from all protocol files.
# Mirrors R&Duck build.sh pattern. Run by CI on every push to main.
# Usage: ./build.sh           → full build (llms-full.txt)
#        ./build.sh llms-only → regenerate llms-full.txt only (for conform.sh freshness check)

set -euo pipefail

V=$(cat VERSION)
OUT="llms-full.txt"

echo "CTRL-AI build v$V"
echo "========================"

# ── ordered file list: load order matters ──
FILES=(
  "CTRL-AI.md"
  "core/kernel.md"
  "core/passage.md"
  "core/security.md"
  "agents/producer.md"
  "agents/ghostwriter.md"
  "agents/researcher.md"
  "agents/auditor.md"
  "agents/strategist.md"
  "modes/research.md"
  "modes/build.md"
