  "modes/orchestrate.md"
  "runtime/state.md"
  "runtime/adapt.md"
  "libraries/personas.md"
  "libraries/domains.md"
  "libraries/audiences.md"
  "adapters/claude.md"
  "adapters/chatgpt.md"
  "adapters/gemini.md"
  "adapters/deepseek.md"
  "adapters/perplexity.md"
  "adapters/local.md"
  "behavior/standard.md"
  "behavior/micro.md"
  "behavior/extended.md"
  "enforcement-ceiling.md"
)

# ── generate llms-full.txt ──
{
  echo "# CTRL-AI v$V — Full Protocol (auto-generated by build.sh)"
  echo "# Do not edit directly. Edit source files and rebuild."
  echo "# Load order: root activator → core → agents → modes → runtime → libraries → adapters → behavior"
  echo "# Generated: $(date -u +%Y-%m-%dT%H:%M:%SZ)"
  echo ""
  for f in "${FILES[@]}"; do
    if [ -f "$f" ]; then
      echo "# ═══════════════════════════════════════════════════════════════"
      echo "# FILE: $f"
      echo "# ═══════════════════════════════════════════════════════════════"
      echo ""
      cat "$f"
      echo ""
      echo ""
    else
      echo "# ⚠ MISSING: $f"
      echo ""
    fi
  done
} > "$OUT"

LINES=$(wc -l < "$OUT")
SIZE=$(wc -c < "$OUT" | awk '{printf "%.1fK", $1/1024}')
echo "✓ $OUT generated: $LINES lines, $SIZE"

# ── generate .nojekyll ──
touch .nojekyll

# ── check/CI mode: regenerate llms-full.txt only (used by conform.sh) ──
if [ "${1:-}" = "llms-only" ]; then exit 0; fi

